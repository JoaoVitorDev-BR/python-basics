# Operadores matemáticos (usando w e x)
w = 35
x = 18

print("Soma:", w + x)             # soma dos valores → 53
print("Subtração:", w - x)        # subtração → 17
print("Multiplicação:", w * x)    # multiplicação → 630
print("Divisão:", w / x)          # divisão normal → 1.944...
print("Divisão inteira:", w // x) # divisão inteira (sem casas decimais) → 1
print("Resto:", w % x)            # resto da divisão → 17
print("Potência:", w ** 2)        # potência (w elevado a 2) → 1225


# Operadores de comparação (usando y e z)
y = 10
z = 60

print("y > z:", y > z)            # maior que → False
print("y < z:", y < z)            # menor que → True
print("y == z:", y == z)          # igual → False
print("y != z:", y != z)          # diferente → True
print("y >= 10:", y >= 10)        # maior ou igual → True
print("z <= 60:", z <= 60)        # menor ou igual → True


# Operadores lógicos (usando feliz e triste)
feliz = True
triste = False

print("feliz AND triste:", feliz and triste)  # AND → True só se ambos forem True → False
print("feliz OR triste:", feliz or triste)    # OR → True se pelo menos um for True → True
print("NOT feliz:", not feliz)                # NOT → inverte o valor → False

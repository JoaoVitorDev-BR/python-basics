# Inteiro: usado para números sem parte decimal
numero = 19
print("Número inteiro:", numero)

# Float: usado para números decimais (ponto flutuante)
metros = 1.80
print("Número decimal (float):", metros)

# String: sequência de caracteres (texto)
nome = "João"
print("Texto (string):", nome)

# Booleano: valores lógicos True (verdadeiro) ou False (falso)
teste = True
print("Valor lógico (booleano):", teste)
